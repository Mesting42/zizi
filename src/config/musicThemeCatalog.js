export const getMusicThemePreset = (presetId) => (
  MUSIC_THEME_PRESETS.find(theme => theme.id === presetId) || MUSIC_THEME_PRESETS[0]
)

export const getMusicThemeIp = (presetId) => getMusicThemePreset(presetId).ip

export const getMusicIp = (ipId) => MUSIC_IPS.find(ip => ip.id === ipId) || MUSIC_IPS[0]

export const getMusicThemeIpLabel = (presetId) => getMusicIp(getMusicThemeIp(presetId)).name

const IP_PLAYLIST_COVERS = {
  'hello-kitty': Array.from(
    { length: 16 },
    (_, index) => `/images/ip-playlists/hello-kitty/cover-${String(index + 1).padStart(2, '0')}.png`
  ),
  kuromi: Array.from(
    { length: 16 },
    (_, index) => `/images/ip-playlists/kuromi/cover-${String(index + 1).padStart(2, '0')}.png`
  )
}

export const getThemedPlaylistCover = (presetId, index = 0, fallback = '') => {
  const ip = getMusicThemeIp(presetId)
  const safeIndex = Math.max(0, Number(index) || 0)

  if (ip === 'shinchan') {
    const coverIndex = (safeIndex % 16) + 1
    return `/images/shinchan-playlists/cover-${String(coverIndex).padStart(2, '0')}.jpg`
  }

  const covers = IP_PLAYLIST_COVERS[ip]
  return covers?.[safeIndex % covers.length] || fallback
}
